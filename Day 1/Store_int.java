class Store_int  
{
	public static void main(String[] args) 
	{
		int a=10;
		byte b=(byte)a;
		short c=(short)a;
		long d=(long)a;
		float e=(float)a;
		double f=(double)a;
		char g=(char)a;
		System.out.println(a);
		System.out.println(b);
		System.out.println(c);
		System.out.println(d);
		System.out.println(e);
		System.out.println(f);
		System.out.println(g);
	}
}
